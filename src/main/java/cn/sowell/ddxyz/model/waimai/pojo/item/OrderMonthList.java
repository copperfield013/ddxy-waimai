package cn.sowell.ddxyz.model.waimai.pojo.item;


public class OrderMonthList {
	private Integer theYear;
	private Integer theMonth;
	private Integer income;
	private Integer orderCount;
	private Integer cupCount;
	
	public Integer getTheYear() {
		return theYear;
	}
	public void setTheYear(Integer theYear) {
		this.theYear = theYear;
	}
	public Integer getTheMonth() {
		return theMonth;
	}
	public void setTheMonth(Integer theMonth) {
		this.theMonth = theMonth;
	}
	public Integer getIncome() {
		return income;
	}
	public void setIncome(Integer income) {
		this.income = income;
	}
	public Integer getOrderCount() {
		return orderCount;
	}
	public void setOrderCount(Integer orderCount) {
		this.orderCount = orderCount;
	}
	public Integer getCupCount() {
		return cupCount;
	}
	public void setCupCount(Integer cupCount) {
		this.cupCount = cupCount;
	}

}
